//Возвращает инвертированную строку, переданною в качестве параметра $a
function func1($a) {
  $r = ''; // инициализация локальной
  $i = strlen($a) // длина строки
          - 1; // получаем индекс последнего символа в строке
  while ($i >= 0) { // перебираем все символы строки в обратном порядке

    $r .= $a[$i];
    --$i;
  }
  return $r;
}

echo "func1(Vlad) = ".func1('Vlad'); // выводим результат работы функции
