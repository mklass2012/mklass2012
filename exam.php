// Возвращает количество символов в строке $a, которые не равны $c
function func2($a, $c) {
  $d = 0;
  for ($i = 0; $i < strlen($a); $i++)
    if ($a[$i] == $c)
      $d++;
  return strlen($a) - $d;
}
echo "func2('Alexey', 'e') = ".func2("Alexey", 'e');
